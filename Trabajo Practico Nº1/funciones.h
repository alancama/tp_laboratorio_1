/** \brief suma dos numeros decimales
 *
 * \param X float (primer operandado)
 * \param Y float (segundo operando)
 * \return float  (resultado de suma)
 *
 */
float suma(float X, float Y);

/** \brief resta dos numeros decimales
 *
 * \param X float (primer operando)
 * \param Y float (segundo operando)
 * \return float  (resultado de resta)
 *
 */
float resta(float X, float Y);

/** \brief division de dos numeros decimales
 *
 * \param X float   dividendo
 * \param Y float   divisor
 * \return float    resultado
 *
 */
float division(float X, float Y);


/** \brief multiplicación de dos numeros decimales
 *
 * \param X float   multiplicando
 * \param Y float   multiplicador
 * \return float    producto
 *
 */
float multiplicacion(float X , float Y );

/** \brief El factoreo de el numero ingresado
 *
 * \param X float numero a ser factoreado
 * \return float resultado de factoreo
 *
 */
int factorial(int n);
